    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        if(ocultarPosicion == true){
            VentanaPrincipal.labelPosicionX.setVisible(false);
            VentanaPrincipal.txtfPosX.setVisible(false);
            VentanaPrincipal.labelPosicionY.setVisible(false);
            VentanaPrincipal.txtfPosY.setVisible(false);
        }
        else{
            VentanaPrincipal.labelPosicionX.setVisible(true);
            VentanaPrincipal.txtfPosX.setVisible(true);
            VentanaPrincipal.labelPosicionY.setVisible(true);
            VentanaPrincipal.txtfPosY.setVisible(true);
        }
        if(ocultaveloc == true){
            VentanaPrincipal.LabelVelocidad.setVisible(false);
            VentanaPrincipal.txtfVel.setVisible(false);
        }
        else{
            VentanaPrincipal.LabelVelocidad.setVisible(true);
            VentanaPrincipal.txtfVel.setVisible(true);   
        }
        if(ocultacele == true){
            VentanaPrincipal.LabelAceleración.setVisible(false);
            VentanaPrincipal.txtfAcel.setVisible(false);
        }
        else{
            VentanaPrincipal.LabelAceleración.setVisible(true);
            VentanaPrincipal.txtfAcel.setVisible(true);   
        }
        if(ocultime == true){
            VentanaPrincipal.LabelTiempo.setVisible(false);
            VentanaPrincipal.txtfTiempo.setVisible(false);
        }
        else{
            VentanaPrincipal.LabelTiempo.setVisible(true);
            VentanaPrincipal.txtfTiempo.setVisible(true);   
        }
        if(ocultBtnReporte){
            VentanaPrincipal.btnReportes.setVisible(false);
        }else{
            VentanaPrincipal.btnReportes.setVisible(true);
        }
        
        if(ocultarVectorPosicion){
            VentanaPrincipal.labelHistoriaPosicion.setVisible(false);
            PlanoCartesiano.mostrarVectorPosicion = false;
        } else {
            VentanaPrincipal.labelHistoriaPosicion.setVisible(true);
            PlanoCartesiano.mostrarVectorPosicion = true;
        }
        
        if(ocultarVectorVelocidad){
            VentanaPrincipal.labelHistoriaVelocidad.setVisible(false);
            PlanoCartesiano.mostrarVectorVelocidad = false;
        } else {
            VentanaPrincipal.labelHistoriaVelocidad.setVisible(true);
            PlanoCartesiano.mostrarVectorVelocidad = true;
        }
        
        if(ocultarVectorAceleracion){
            VentanaPrincipal.labelHistoriaAceleracion.setVisible(false);
            PlanoCartesiano.mostrarVectorAceleracion = false;
        } else {
            VentanaPrincipal.labelHistoriaAceleracion.setVisible(true);
            PlanoCartesiano.mostrarVectorAceleracion = true;
        }
        
        if(inhabilitarOverflow){
            PlanoCartesiano.detenerSiEscapa = false;
        } else {
            PlanoCartesiano.detenerSiEscapa = true;
        }
        
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

