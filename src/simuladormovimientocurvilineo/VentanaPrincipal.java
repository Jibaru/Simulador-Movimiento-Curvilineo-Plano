    private boolean esParseable(String ecuacion){
        String[] elementos = ecuacion.split("[+]");
        int validaciones = 0;
        for (String elemento : elementos) {
            if(elemento.split("[*^]").length == 3){
                validaciones++;
            }
        }
        if(validaciones == elementos.length) return true;

        return false;
    }
